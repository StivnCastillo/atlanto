<?php 

class Estadoticket_model extends CI_Model {
	private $tabla = 'ticket_estado';

	function __construct() {
        // Call the Model constructor
        parent::__construct();
    }
    
    function get_todos() {
        $query = $this->db->get($this->db->dbprefix($this->tabla));
        if ($query->num_rows() > 0){
            return $query->result();
        }else{
            return FALSE;
        }        
    }

    function get_estado($data) {
        $this->db->where($data);
        $query = $this->db->get($this->db->dbprefix($this->tabla));
        if ($query->num_rows() > 0){
            return $query->row();
        }else{
            return FALSE;
        }
    }
}